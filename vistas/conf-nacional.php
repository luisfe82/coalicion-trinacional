                            pública como un derecho social a participar en la Conferencia Nacional en Defensa de la
                            Educación Pública 2022, en donde realizaremos un balance de la situación actual de la
                            educación en nuestro país, considerando el horizonte internacional, y construiremos juntos
                            propuestas para la defensa de la educación pública y gratuita en América, ante las
