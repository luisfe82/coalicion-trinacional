                            superar los problemas provocados por ella e identificar las alternativas para reconstruir a
                            las comunidades educativas que han sido afectadas por el distanciamiento social y el
                            auto-centramiento provocado por el confinamiento; al mismo tiempo, necesitamos recuperar las
