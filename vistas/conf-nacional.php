                            garantizará que todos los niños y jóvenes regresen a la escuela. Puesto que, muchos
                            problemas que observamos hoy en el sector educativo son preexistentes a ella; la
                            vulnerabilidad a la que se enfrentan los niños, familias y comunidades de bajos ingresos, la
                            desigual cobertura en educación, la discriminación y exclusión por raza, clase y género, la
                            precariedad salarial y de condiciones de trabajo de los docentes, entre muchos otros
